import SwiftUI
import PhotosUI
import Photos
import SwiftyCrop

struct ProfilePhotoStepView: View {
    @ObservedObject var viewModel: OnboardingViewModel
    @State private var selectedImage: UIImage?
    @State private var croppedImage: UIImage?
    @State private var showingGalleryPicker = false        // Picker standard (acc√®s complet)
    @State private var showingLimitedGalleryView = false   // Interface personnalis√©e (acc√®s limit√©)
    @State private var showImageCropper = false           // SwiftyCrop
    @State private var showSettingsAlert = false          // Alerte param√®tres
    @State private var limitedPhotoAssets: [PHAsset] = [] // Photos autoris√©es
    @State private var alertMessage = ""
    
    var body: some View {
        VStack(spacing: 0) {
            // Espace entre barre de progression et titre (harmonis√©)
            Spacer()
                .frame(height: 40)
            
            // Titre centr√© √† gauche
            HStack {
                Text("add_profile_photo".localized)
                    .font(.system(size: 36, weight: .bold))
                    .foregroundColor(.black)
                    .multilineTextAlignment(.leading)
                Spacer()
            }
            .padding(.horizontal, 30)
            
            // Premier Spacer pour centrer le contenu
            Spacer()
            
            // Contenu principal centr√©
            VStack(spacing: 50) {
                // Photo de profil cliquable avec gestion des permissions
                Button(action: {
                    checkPhotoLibraryPermission()
                }) {
                    ZStack {
                        Circle()
                            .fill(Color.white)
                            .frame(width: 160, height: 160)
                            .shadow(color: Color.black.opacity(0.1), radius: 15, x: 0, y: 8)
                        
                        if let croppedImage = croppedImage {
                            Image(uiImage: croppedImage)
                                .resizable()
                                .aspectRatio(contentMode: .fill)
                                .frame(width: 160, height: 160)
                                .clipShape(Circle())
                        } else {
                            VStack(spacing: 8) {
                                Image(systemName: "person.fill")
                                    .font(.system(size: 40))
                                    .foregroundColor(.black.opacity(0.3))
                                
                                Text("add_photo".localized)
                                    .font(.system(size: 14))
                                    .foregroundColor(.black.opacity(0.6))
                            }
                        }
                    }
                }
                .buttonStyle(PlainButtonStyle())
            }
            
            // Deuxi√®me Spacer pour pousser la zone bouton vers le bas
            Spacer()
                
            // Zone blanche coll√©e en bas
            VStack(spacing: 15) {
                Button(action: {
                    if let image = croppedImage {
                        viewModel.profileImage = image
                    }
                    viewModel.nextStep()
                }) {
                                            Text("continue".localized)
                        .font(.system(size: 18, weight: .semibold))
                        .foregroundColor(.white)
                        .frame(maxWidth: .infinity)
                        .frame(height: 56)
                        .background(Color(hex: "#FD267A"))
                        .cornerRadius(28)
                }
                .padding(.horizontal, 30)
                
                // Bouton "Passer cette √©tape" maintenant en dessous du bouton Continuer
                Button(action: {
                    viewModel.nextStep()
                }) {
                    Text("skip_step".localized)
                        .font(.system(size: 16))
                        .foregroundColor(.black.opacity(0.6))
                        .underline()
                }
            }
            .padding(.vertical, 30)
            .background(Color.white)
        }
        .sheet(isPresented: $showingGalleryPicker) {
            StandardGalleryPicker(onImageSelected: handleImageSelection)
        }
        .sheet(isPresented: $showingLimitedGalleryView) {
            LimitedGalleryView(assets: limitedPhotoAssets, onImageSelected: handleImageSelection)
        }
        .fullScreenCover(isPresented: $showImageCropper) {
            if let imageToProcess = selectedImage {
                SwiftyCropView(
                    imageToCrop: imageToProcess,
                    maskShape: .circle,
                    configuration: SwiftyCropConfiguration(
                        maxMagnificationScale: 4.0,
                        maskRadius: 150,
                        cropImageCircular: true,
                        rotateImage: false,
                        rotateImageWithButtons: false,
                        zoomSensitivity: 1.0,
                        texts: SwiftyCropConfiguration.Texts(
                                            cancelButton: "cancel".localized,
                interactionInstructions: "crop_photo_instructions".localized,
                saveButton: "validate".localized
                        )
                    )
                ) { resultImage in
                    print("üî•üî•üî• SWIFTYCROP: Callback appel√© avec image cropp√©e (optionnel ?: \(resultImage != nil))")
                    guard let finalImage = resultImage else {
                        print("‚ùå SWIFTYCROP: L'image cropp√©e est nil")
                        self.showImageCropper = false
                        return
                    }
                    print("üî•üî•üî• SWIFTYCROP: Cropped image size = \(finalImage.size)")
                    self.croppedImage = finalImage
                    self.viewModel.profileImage = finalImage
                    print("üî•üî•üî• SWIFTYCROP: Fermeture du cropper...")
                    self.showImageCropper = false
                }
                .onAppear {
                    print("üî•üî•üî• SWIFTYCROP: fullScreenCover est en train de s'afficher")
                    print("üî•üî•üî• SWIFTYCROP: showImageCropper = \(showImageCropper)")
                    print("üî•üî•üî• SWIFTYCROP: selectedImage existe = \(selectedImage != nil)")
                    if let img = selectedImage {
                        print("üî•üî•üî• SWIFTYCROP: Image size = \(img.size)")
                    }
                    print("üî•üî•üî• SWIFTYCROP: SwiftyCropView.onAppear() appel√©")
                }
                .onDisappear {
                    print("üî•üî•üî• SWIFTYCROP: SwiftyCropView.onDisappear() appel√©")
                }
            } else {
                // Afficher une vue d'erreur au lieu d'un √©cran blanc
                VStack {
                    Text("error_image_not_found".localized)
                        .font(.title)
                        .foregroundColor(.red)
                    
                    Button("close".localized) {
                        print("üî•üî•üî• SWIFTYCROP: Fermeture forc√©e du cropper")
                        self.showImageCropper = false
                    }
                    .foregroundColor(.white)
                    .padding()
                    .background(Color.blue)
                    .cornerRadius(10)
                }
                .frame(maxWidth: .infinity, maxHeight: .infinity)
                .background(Color.black)
                .onAppear {
                    print("‚ùå‚ùå‚ùå SWIFTYCROP: ERREUR - selectedImage est nil!")
                }
            }
        }
        .alert(isPresented: $showSettingsAlert) {
            Alert(
                                    title: Text(NSLocalizedString("authorization_required", comment: "Authorization required title")),
                message: Text(alertMessage),
                primaryButton: .default(Text(NSLocalizedString("open_settings_button", comment: "Open settings button"))) {
                    openSettings()
                },
                                    secondaryButton: .cancel(Text(NSLocalizedString("cancel", comment: "Cancel button")))
            )
        }
        .onChange(of: showImageCropper) { _, newValue in
            print("üî•üî•üî• SWIFTYCROP: onChange showImageCropper = \(newValue)")
        }
        .onChange(of: selectedImage) { _, newImage in
            print("üî•üî•üî• SWIFTYCROP: onChange selectedImage = \(newImage != nil)")
            if let image = newImage {
                print("üî•üî•üî• SWIFTYCROP: Nouvelle image s√©lectionn√©e, size = \(image.size)")
            }
        }
    }
    
    // MARK: - Gestion des permissions selon le guide
    
    private func checkPhotoLibraryPermission() {
        print("üîê ProfilePhoto: V√©rification des autorisations de la phototh√®que")
        let status = PHPhotoLibrary.authorizationStatus(for: .readWrite)
        print("üì± ProfilePhoto: Statut actuel: \(status.toString())")
        
        switch status {
        case .authorized:
            // ‚úÖ ACC√àS COMPLET
            print("‚úÖ ProfilePhoto: Acc√®s complet d√©j√† autoris√©")
            showingGalleryPicker = true
            
        case .limited:
            // ‚úÖ ACC√àS LIMIT√â - Charger les photos autoris√©es
            print("üîç ProfilePhoto: Acc√®s limit√© d√©tect√©")
            loadLimitedAssets { success in
                DispatchQueue.main.async {
                    if success {
                        self.showingLimitedGalleryView = true
                    } else {
                        // Fallback vers picker standard si √©chec
                        self.showingGalleryPicker = true
                    }
                }
            }
            
        case .notDetermined:
            // ‚è≥ PREMI√àRE DEMANDE
            print("‚è≥ ProfilePhoto: Premi√®re demande d'autorisation")
            PHPhotoLibrary.requestAuthorization(for: .readWrite) { newStatus in
                DispatchQueue.main.async {
                    print("üì± ProfilePhoto: Nouveau statut: \(newStatus.toString())")
                    // R√©cursion pour traiter le nouveau statut
                    self.checkPhotoLibraryPermission()
                }
            }
            
        case .denied, .restricted:
            // ‚ùå ACC√àS REFUS√â - Proposer d'aller aux param√®tres
            print("‚ùå ProfilePhoto: Acc√®s refus√©")
            alertMessage = "photo_access_denied_message".localized
            showSettingsAlert = true
            
        @unknown default:
            print("‚ùì ProfilePhoto: Statut inconnu")
            alertMessage = "photo_access_error_generic".localized
            showSettingsAlert = true
        }
    }
    
    private func loadLimitedAssets(completion: @escaping (Bool) -> Void) {
        print("üì∏ ProfilePhoto: Chargement des photos autoris√©es...")
        
        let fetchOptions = PHFetchOptions()
        fetchOptions.sortDescriptors = [NSSortDescriptor(key: "creationDate", ascending: false)]
        
        // ‚úÖ MAGIE iOS : Cette ligne ne retourne QUE les photos autoris√©es
        let allPhotos = PHAsset.fetchAssets(with: .image, options: fetchOptions)
        print("üì∏ ProfilePhoto: Nombre de photos accessibles: \(allPhotos.count)")
        
        limitedPhotoAssets = []
        
        if allPhotos.count > 0 {
            for i in 0..<allPhotos.count {
                let asset = allPhotos.object(at: i)
                limitedPhotoAssets.append(asset)
            }
            completion(true)
        } else {
            print("‚ùå ProfilePhoto: Aucune photo accessible")
            completion(false)
        }
    }
    
    private func handleImageSelection(_ imageData: UIImage) {
        print("‚úÖ ProfilePhoto: Image s√©lectionn√©e, ouverture du cropper")
        print("üî•üî•üî• SWIFTYCROP: handleImageSelection appel√©")
        print("üî•üî•üî• SWIFTYCROP: Image re√ßue, size = \(imageData.size)")
        
        selectedImage = imageData
        
        // Fermer la sheet de s√©lection
        showingGalleryPicker = false
        showingLimitedGalleryView = false
        
        print("üî•üî•üî• SWIFTYCROP: Avant d'activer showImageCropper")
        
        // Petit d√©lai pour s'assurer que les sheets sont ferm√©es
        DispatchQueue.main.asyncAfter(deadline: .now() + 0.1) {
            print("üî•üî•üî• SWIFTYCROP: Activation de showImageCropper...")
            self.showImageCropper = true
            print("üî•üî•üî• SWIFTYCROP: showImageCropper activ√© = \(self.showImageCropper)")
        }
    }
    
    private func openSettings() {
        if let url = URL(string: UIApplication.openSettingsURLString) {
            UIApplication.shared.open(url)
        }
    }
}

