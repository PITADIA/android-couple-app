import Foundation
import AuthenticationServices
import FirebaseAuth
import CryptoKit

class AuthenticationService: NSObject, ObservableObject {
    static let shared = AuthenticationService()
    
    @Published var isAuthenticated: Bool = false
    @Published var isLoading: Bool = false
    @Published var errorMessage: String?
    @Published var currentUser: FirebaseAuth.User?
    @Published var appleUserDisplayName: String?
    
    private var currentNonce: String?
    private var isSignInInProgress = false
    private var lastProcessedCredentialTime = Date.distantPast
    
    override init() {
        super.init()
        
        // V√©rifier si l'utilisateur est d√©j√† connect√©
        if let user = Auth.auth().currentUser {
            print("üî• AuthenticationService: Utilisateur d√©j√† connect√©: \(user.uid)")
            NSLog("üî• AuthenticationService: Utilisateur d√©j√† connect√©: \(user.uid)")
            self.currentUser = user
            self.isAuthenticated = true
            
            // Charger le nom Apple sauvegard√©
            loadSavedAppleDisplayName(for: user.uid)
        }
        
        // √âcouter les changements d'authentification
        _ = Auth.auth().addStateDidChangeListener { [weak self] _, user in
            DispatchQueue.main.async {
                self?.currentUser = user
                self?.isAuthenticated = user != nil
                
                if let user = user {
                    print("‚úÖ Utilisateur connect√©")
                    // Charger le nom Apple sauvegard√© pour cet utilisateur
                    self?.loadSavedAppleDisplayName(for: user.uid)
                } else {
                    print("‚ùå Utilisateur d√©connect√©")
                }
            }
        }
    }
    
    // MARK: - Public Methods
    
    func signInWithApple() {
        // Protection contre les appels multiples
        guard !isSignInInProgress else {
            print("‚ö†Ô∏è Sign In d√©j√† en cours")
            return
        }
        
        // Protection contre les appels trop fr√©quents (moins de 2 secondes)
        let timeSinceLastCall = Date().timeIntervalSince(lastProcessedCredentialTime)
        guard timeSinceLastCall > 2.0 else {
            print("‚ö†Ô∏è Appels trop fr√©quents")
            return
        }
        
        print("üîê D√©but authentification Apple")
        
        isSignInInProgress = true
        
        let nonce = randomNonceString()
        currentNonce = nonce
        
        let appleIDProvider = ASAuthorizationAppleIDProvider()
        let request = appleIDProvider.createRequest()
        request.requestedScopes = [.fullName, .email]
        request.nonce = sha256(nonce)
        
        let authorizationController = ASAuthorizationController(authorizationRequests: [request])
        authorizationController.delegate = self
        authorizationController.presentationContextProvider = self
        authorizationController.performRequests()
        
        isLoading = true
        errorMessage = nil
    }
    
    func signOut() {
        print("üî• SignOut: signOut() appel√©")
        print("üî• SignOut: Thread: \(Thread.current)")
        print("üî• SignOut: Stack trace: \(Thread.callStackSymbols.prefix(5))")
        print("üî• AuthenticationService: D√©connexion")
        NSLog("üî• AuthenticationService: D√©connexion")
        
        do {
            try Auth.auth().signOut()
            // Nettoyer le nom Apple sauvegard√©
            DispatchQueue.main.async {
                self.appleUserDisplayName = nil
            }
        } catch {
            print("üî• AuthenticationService: Erreur de d√©connexion: \(error.localizedDescription)")
            NSLog("üî• AuthenticationService: Erreur de d√©connexion: \(error.localizedDescription)")
            errorMessage = error.localizedDescription
        }
    }
    
    // MARK: - Private Methods
    
    private func randomNonceString(length: Int = 32) -> String {
        precondition(length > 0)
        let charset: [Character] =
        Array("0123456789ABCDEFGHIJKLMNOPQRSTUVXYZabcdefghijklmnopqrstuvwxyz-._")
        var result = ""
        var remainingLength = length
        
        while remainingLength > 0 {
            let randoms: [UInt8] = (0 ..< 16).map { _ in
                var random: UInt8 = 0
                let errorCode = SecRandomCopyBytes(kSecRandomDefault, 1, &random)
                if errorCode != errSecSuccess {
                    fatalError("Unable to generate nonce. SecRandomCopyBytes failed with OSStatus \(errorCode)")
                }
                return random
            }
            
            randoms.forEach { random in
                if remainingLength == 0 {
                    return
                }
                
                if random < charset.count {
                    result.append(charset[Int(random)])
                    remainingLength -= 1
                }
            }
        }
        
        return result
    }
    
    private func sha256(_ input: String) -> String {
        let inputData = Data(input.utf8)
        let hashedData = SHA256.hash(data: inputData)
        let hashString = hashedData.compactMap {
            String(format: "%02x", $0)
        }.joined()
        
        return hashString
    }
    
    // MARK: - Apple Display Name Persistence
    
    private func loadSavedAppleDisplayName(for userID: String) {
        let savedName = UserDefaults.standard.string(forKey: "AppleDisplayName_\(userID)")
        if let savedName = savedName, !savedName.isEmpty {
            print("üì± Nom Apple restaur√©")
            DispatchQueue.main.async {
                self.appleUserDisplayName = savedName
            }
        } else {
            DispatchQueue.main.async {
                self.appleUserDisplayName = nil
            }
        }
    }
}

// MARK: - ASAuthorizationControllerDelegate

extension AuthenticationService: ASAuthorizationControllerDelegate {
    func authorizationController(controller: ASAuthorizationController, didCompleteWithAuthorization authorization: ASAuthorization) {
        // Protection contre le traitement multiple
        let timeSinceLastProcessing = Date().timeIntervalSince(lastProcessedCredentialTime)
        guard timeSinceLastProcessing > 1.0 else {
            print("‚ö†Ô∏è Credentials d√©j√† trait√©s")
            return
        }
        
        lastProcessedCredentialTime = Date()
        print("‚úÖ Autorisation Apple re√ßue")
        
        if let appleIDCredential = authorization.credential as? ASAuthorizationAppleIDCredential {
            
            // R√©cup√©rer le nom fourni par Apple (si disponible)
            if let fullName = appleIDCredential.fullName {
                let displayName = PersonNameComponentsFormatter.localizedString(from: fullName, style: .long)
                if !displayName.isEmpty {
                    print("‚úÖ Nom Apple r√©cup√©r√©")
                    // Sauvegarder le nom dans UserDefaults pour les futures connexions
                    let userID = appleIDCredential.user
                    UserDefaults.standard.set(displayName, forKey: "AppleDisplayName_\(userID)")
                    DispatchQueue.main.async {
                        self.appleUserDisplayName = displayName
                    }
                } else {
                    // Essayer de charger depuis UserDefaults
                    loadSavedAppleDisplayName(for: appleIDCredential.user)
                }
            } else {
                // Pas de nom fourni, essayer de charger depuis UserDefaults
                loadSavedAppleDisplayName(for: appleIDCredential.user)
            }
            
            guard let nonce = currentNonce else {
                print("‚ùå Nonce invalide")
                DispatchQueue.main.async {
                    self.isLoading = false
                    self.isSignInInProgress = false
                    self.errorMessage = "Erreur d'authentification"
                }
                return
            }
            
            guard let appleIDToken = appleIDCredential.identityToken else {
                print("‚ùå Token Apple manquant")
                DispatchQueue.main.async {
                    self.isLoading = false
                    self.isSignInInProgress = false
                    self.errorMessage = "Token manquant"
                }
                return
            }
            
            guard let idTokenString = String(data: appleIDToken, encoding: .utf8) else {
                print("‚ùå Erreur s√©rialisation token")
                DispatchQueue.main.async {
                    self.isLoading = false
                    self.isSignInInProgress = false
                    self.errorMessage = "Erreur token"
                }
                return
            }
            
            // Cr√©er les credentials Firebase
            let credential = OAuthProvider.credential(providerID: AuthProviderID.apple,
                                                      idToken: idTokenString,
                                                      rawNonce: nonce)
            
            // Se connecter √† Firebase
            Auth.auth().signIn(with: credential) { [weak self] result, error in
                DispatchQueue.main.async {
                    self?.isLoading = false
                    self?.isSignInInProgress = false
                    
                    if let error = error {
                        print("‚ùå Erreur Firebase: \(error.localizedDescription)")
                        self?.errorMessage = error.localizedDescription
                        return
                    }
                    
                    if let user = result?.user {
                        print("‚úÖ Connexion Firebase r√©ussie")
                        
                        // Notifier le succ√®s de l'authentification
                        NotificationCenter.default.post(
                            name: NSNotification.Name("UserAuthenticated"),
                            object: nil
                        )
                    }
                }
            }
        }
    }
    
    func authorizationController(controller: ASAuthorizationController, didCompleteWithError error: Error) {
        DispatchQueue.main.async {
            self.isLoading = false
            self.isSignInInProgress = false
            
            if let authError = error as? ASAuthorizationError {
                switch authError.code {
                case .canceled:
                    print("‚ö†Ô∏è Authentification annul√©e")
                    // Ne pas afficher d'erreur pour une annulation
                    return
                case .failed:
                    self.errorMessage = "√âchec de l'authentification"
                case .invalidResponse:
                    self.errorMessage = "R√©ponse invalide"
                case .notHandled:
                    self.errorMessage = "Authentification non g√©r√©e"
                case .unknown:
                    self.errorMessage = "Erreur inconnue"
                default:
                    self.errorMessage = "Erreur d'authentification"
                }
                print("‚ùå Erreur authentification: \(self.errorMessage ?? "inconnue")")
            } else {
                self.errorMessage = "Erreur syst√®me"
                print("‚ùå Erreur syst√®me")
            }
        }
    }
}

// MARK: - ASAuthorizationControllerPresentationContextProviding

extension AuthenticationService: ASAuthorizationControllerPresentationContextProviding {
    func presentationAnchor(for controller: ASAuthorizationController) -> ASPresentationAnchor {
        guard let windowScene = UIApplication.shared.connectedScenes.first as? UIWindowScene,
              let window = windowScene.windows.first else {
            fatalError("No window available")
        }
        return window
    }
}